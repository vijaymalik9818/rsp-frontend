                    <h3>The Privacy Code of Real Estate Professionals Inc.</h3>
                    <p>
                        It is the policy of Real Estate Professionals Inc. to be open and accountable and to protect the personal information of members of the public and our associates. Real Estate Professionals Inc. is responsible for maintaining and protecting all personal information that is in its custody or under its control. In carrying out its responsibility, Real Estate Professionals Inc. will do what a reasonable person would think is appropriate in the circumstances. All employees and associates associated with this office will comply with the requirements of this Code.
                    </p>

                    <h3>The Policy Statement</h3>
                    <p>
                        This office only collects personal information necessary to comply with the law (ex: satisfy FINTRAC regime obligations), to effectively market and sell the property of sellers, to locate, assess and qualify properties for buyers and to otherwise provide professional and competent real estate services to clients and customers. Depending on the circumstances, Real Estate Professionals Inc. may record personal information by way of digital recording or other technique. The recording may be collected, used and disclosed with or without the consent of the individual in accordance with the Personal Information Protection Act (PIPA), the Real Estate Act (REA), its regulations, bylaws and rules.
                    </p>

                    <h3>The Person In Charge</h3>
                    <p>
                        Pranav Khemka, CEO, is the privacy compliance officer responsible for privacy compliance in this office. His/her name shall be made available to consumers. The responsibilities of the privacy compliance officer shall include:
                    </p>

                    <ol style="list-style-type:lower-roman">
                        <li>Establish and update information protection policies.</li>
                        <li>Ensure policies are implemented by other organizations to which data-processing functions are outsourced.</li>
                        <li>Establish criteria for classification of information.</li>
                        <li>Evaluate the accessibility of sensitive information and take corrective action where necessary.</li>
                        <li>Provide education to employees on the importance of information protection.</li>
                        <li>Attempt to resolve consumer privacy complaints to the reasonable satisfaction of the consumer.</li>
                    </ol>

                    <p>Respond to privacy breaches, including reporting breaches to impacted individuals, the relevant privacy commissioner(s), and any other government institution when notifying that institution may be able to reduce the harm from the breach, when necessary.</p>

                    <h3>The Collection, Use and Disclosure of Personal Information</h3>

                    <ol style="list-style-type:lower-roman">
                        <li>Real Estate Professionals Inc. will only collect information for purposes that are reasonable and only to the extent that is reasonable for meeting the purposes for which the information&rsquo;s collected.
                        </li>
                        <li>Only the information necessary to facilitate the real estate transaction or otherwise provide professional and competent services to clients and customers will be collected.</li>
                        <li>No personal information shall be collected from an individual without first obtaining the consent of the individual to the collection, use and dissemination of that information.</li>
                        <li>Express consent (whether oral or written) must always be obtained except in the following situation: consent may be implied where the information is not sensitive and where it can be reasonably assumed that the individual would expect the information to be disclosed in this fashion, or those listed below in 4(g).</li>
                        <li>Once information is collected, it will be used and disclosed only for the purposes disclosed to the individual.</li>
                        <li>All representation agreements must include the approved privacy clauses.</li>
                        <li>Information is collected directly from the individual the information is about unless the individual consents to the collection of information from another source, or the information may be collected without consent under, but not limited to, the following circumstances:
                            <ol style="list-style-type:lower-roman">
                                <li>When a reasonable person would consider that it is clearly in the interests of the individual and Real Estate Professionals Inc. cannot obtain consent in a timely way or the individual would not reasonably be expected to hold back consent.</li>
                                <li>When the <em>REA</em>, <em>PIPA</em> , or another Act or regulation requires or allows for collection without consent.</li>
                                <li>If the collection is reasonable for the purposes of an investigation or legal proceeding.</li>
                                <li>If the information is needed to collect a debt owing to Real Estate Professionals Inc.</li>
                                <li>If the information is publicly available as defined in the <em>PIPA Regulation</em>.</li>
                                <li>If the information can be disclosed to Real Estate Professionals Inc. without consent. Or;</li>
                                <li>If the collection meets the requirements for archival purposes or research set out in the <em>PIPA Regulation</em> and is not reasonable to obtain the individual&rsquo;s consent.</li>
                            </ol>
                        </li>
                    </ol>

